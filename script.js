// Mostrar/Ocultar los proyectos adicionales
// Selección del botón y la sección de proyectos ocultos
document.addEventListener("DOMContentLoaded", function () {
    // Obtener el botón y la sección de proyectos adicionales
    var toggleButton = document.getElementById("toggleButton");
    var moreProjects = document.getElementById("moreProjects");

    // Controlar la visibilidad de los proyectos adicionales al hacer clic en el botón
    toggleButton.addEventListener("click", function () {
        if (moreProjects.style.display === "none" || moreProjects.style.display === "") {
            moreProjects.style.display = "grid"; // Mostrar los proyectos
            toggleButton.textContent = "Show Less Projects"; // Cambiar texto del botón
        } else {
            moreProjects.style.display = "none"; // Ocultar los proyectos
            toggleButton.textContent = "Show More Projects"; // Cambiar texto del botón
        }
    });
});


